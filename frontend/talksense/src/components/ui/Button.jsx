import { cn } from "../../lib/utils";

/**
 * Button Component
 * @param {string} variant - primary, secondary, ghost, outline, destructive
 * @param {string} size - sm, md, lg
 * @param {boolean} isLoading - Shows loading spinner
 * @param {string} className - Additional classes
 */
export const Button = ({
  children,
  className,
  variant = "primary",
  size = "md",
  isLoading = false,
  disabled,
  type = "button",
  ...props
}) => {
  const baseStyles = "inline-flex items-center justify-center rounded-lg font-medium transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 active:scale-95";
  
  const variants = {
    primary: "bg-[var(--color-primary)] text-white hover:bg-[var(--color-primary-hover)] shadow-md shadow-blue-500/20 border border-transparent",
    secondary: "bg-white text-[var(--color-text-main)] border border-[var(--color-border)] hover:bg-slate-50 shadow-sm",
    ghost: "bg-transparent hover:bg-slate-100 text-[var(--color-text-main)]",
    outline: "border border-[var(--color-primary)] text-[var(--color-primary)] bg-transparent hover:bg-blue-50",
    destructive: "bg-red-600 text-white hover:bg-red-700 shadow-md shadow-red-500/20",
    accent: "bg-[var(--color-accent)] text-white hover:bg-amber-600 shadow-md shadow-amber-500/20",
  };

  const sizes = {
    sm: "h-8 px-3 text-xs",
    md: "h-10 px-4 py-2",
    lg: "h-12 px-6 text-lg",
  };

  return (
    <button
      type={type}
      className={cn(baseStyles, variants[variant], sizes[size], className)}
      disabled={disabled || isLoading}
      {...props}
    >
      {isLoading && (
        <svg className="mr-2 h-4 w-4 animate-spin" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
          <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
          <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
        </svg>
      )}
      {children}
    </button>
  );
};
